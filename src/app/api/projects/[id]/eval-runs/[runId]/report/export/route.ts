import { NextResponse } from 'next/server'
import { prisma } from '@/lib/db/client'
import { createClient } from '@/lib/supabase/server'
import type { RankingEntry, ScoreMatrixEntry, HighlightEntry, CostProjectionEntry } from '@/lib/eval/types'

export async function GET(
  req: Request,
  { params }: { params: { id: string; runId: string } }
) {
  const supabase = createClient()
  const { data: { user } } = await supabase.auth.getUser()
  if (!user) return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })

  const project = await prisma.evalProject.findFirst({
    where: { id: params.id, userId: user.id },
  })
  if (!project) return NextResponse.json({ error: 'Not found' }, { status: 404 })

  const report = await prisma.evalReport.findUnique({
    where: { evalRunId: params.runId },
  })
  if (!report) return NextResponse.json({ error: 'Report not found' }, { status: 404 })

  const { searchParams } = new URL(req.url)
  const format = searchParams.get('format') || 'md'

  const rankings = report.rankings as unknown as RankingEntry[]
  const scoreMatrix = report.scoreMatrix as unknown as ScoreMatrixEntry[]
  const highlights = report.highlights as unknown as HighlightEntry[]
  const costProjection = report.costProjection as unknown as CostProjectionEntry[]

  if (format === 'md') {
    const markdown = generateMarkdown(
      project.name,
      rankings,
      scoreMatrix,
      highlights,
      costProjection,
      report.recommendation
    )
    return new Response(markdown, {
      headers: {
        'Content-Type': 'text/markdown; charset=utf-8',
        'Content-Disposition': `attachment; filename="eval-report-${params.runId}.md"`,
      },
    })
  }

  // PDF export — return markdown for now (client-side PDF via @react-pdf/renderer)
  return NextResponse.json({
    rankings,
    scoreMatrix,
    highlights,
    costProjection,
    recommendation: report.recommendation,
    projectName: project.name,
  })
}

function generateMarkdown(
  projectName: string,
  rankings: RankingEntry[],
  scoreMatrix: ScoreMatrixEntry[],
  highlights: HighlightEntry[],
  costProjection: CostProjectionEntry[],
  recommendation: string
): string {
  const lines: string[] = []

  lines.push(`# AI モデル評価レポート: ${projectName}`)
  lines.push(``)
  lines.push(`生成日: ${new Date().toISOString().split('T')[0]}`)
  lines.push(``)

  // Section 1: Rankings
  lines.push(`## 1. 推奨モデルと総合ランキング`)
  lines.push(``)
  if (rankings.length > 0) {
    lines.push(`**推奨モデル: ${rankings[0].model}** (総合スコア: ${rankings[0].totalScore}/10)`)
    lines.push(``)
  }
  lines.push(`| 順位 | モデル | 総合スコア |`)
  lines.push(`|------|--------|------------|`)
  for (const r of rankings) {
    lines.push(`| ${r.rank} | ${r.model} | ${r.totalScore}/10 |`)
  }
  lines.push(``)

  // Section 2: Score Matrix
  lines.push(`## 2. スコア比較テーブル`)
  lines.push(``)
  if (scoreMatrix.length > 0) {
    const metrics = Object.keys(scoreMatrix[0].metrics)
    lines.push(`| 指標 | ${scoreMatrix.map(s => s.model).join(' | ')} |`)
    lines.push(`|------|${scoreMatrix.map(() => '------').join('|')}|`)
    for (const metric of metrics) {
      const values = scoreMatrix.map(s => {
        const v = s.metrics[metric]
        return v !== undefined ? v.toFixed(1) : '-'
      })
      lines.push(`| ${metric} | ${values.join(' | ')} |`)
    }
  }
  lines.push(``)

  // Section 4: Highlights
  lines.push(`## 3. 注目すべき回答例`)
  lines.push(``)
  for (const h of highlights) {
    lines.push(`### Q: ${h.prompt}`)
    lines.push(`カテゴリ: ${h.category}`)
    lines.push(``)
    for (const r of h.responses) {
      lines.push(`**${r.model}** (スコア: ${r.totalScore.toFixed(1)})`)
      lines.push(`> ${r.content.slice(0, 200)}${r.content.length > 200 ? '...' : ''}`)
      lines.push(``)
    }
  }

  // Section 5: Cost Projection
  lines.push(`## 4. コスト試算`)
  lines.push(``)
  lines.push(`| モデル | 1,000回あたりコスト | 中央値入力トークン | 中央値出力トークン |`)
  lines.push(`|--------|---------------------|---------------------|---------------------|`)
  for (const c of costProjection) {
    lines.push(`| ${c.model} | $${c.costPer1000.toFixed(3)} | ${c.medianInputTokens} | ${c.medianOutputTokens} |`)
  }
  lines.push(``)

  // Section 6: Recommendation
  lines.push(`## 5. 推奨アクション`)
  lines.push(``)
  lines.push(recommendation)
  lines.push(``)
  lines.push(`---`)
  lines.push(`*Generated by Platon AI Eval*`)

  return lines.join('\n')
}
